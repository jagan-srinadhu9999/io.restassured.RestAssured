package com.pojo;

public class Marks {
	
	private int telugu;
	private int english;
	private int maths;
	private int science;
	private int social;
	private int hindi;
	private int ed;
	private int kom;
	private int te;
	
	public int getTelugu() {
		return telugu;
	}
	public void setTelugu(int telugu) {
		this.telugu = telugu;
	}
	public int getEnglish() {
		return english;
	}
	public void setEnglish(int english) {
		this.english = english;
	}
	public int getMaths() {
		return maths;
	}
	public void setMaths(int maths) {
		this.maths = maths;
	}
	public int getScience() {
		return science;
	}
	public void setScience(int science) {
		this.science = science;
	}
	public int getSocial() {
		return social;
	}
	public void setSocial(int social) {
		this.social = social;
	}
	public int getHindi() {
		return hindi;
	}
	public void setHindi(int hindi) {
		this.hindi = hindi;
	}
	public int getEd() {
		return ed;
	}
	public void setEd(int ed) {
		this.ed = ed;
	}
	public int getKom() {
		return kom;
	}
	public void setKom(int kom) {
		this.kom = kom;
	}
	public int getTe() {
		return te;
	}
	public void setTe(int te) {
		this.te = te;
	}
	
	public Marks(int telugu, int english, int maths, int science, int social, int hindi, int ed, int kom, int te) {
		
		this.telugu = telugu;
		this.english = english;
		this.maths = maths;
		this.science = science;
		this.social = social;
		this.hindi = hindi;
		this.ed = ed;
		this.kom = kom;
		this.te = te;
	}
	

  	
}
